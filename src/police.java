    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String var = jTextField1.getText();
        DefaultTableModel m1 = (DefaultTableModel) jTable1.getModel();
        m1.setRowCount(0);
        if (jRadioButton1.isSelected()){
            ResultSet rs = Police.searchByCity(var);
            try {
                if(!rs.next())
                    JOptionPane.showMessageDialog(this, "No record found");
                rs.beforeFirst();
                while(rs.next()) {
                    int id = rs.getInt("policeID");
                    String name = rs.getString("Name");
                    String contact = rs.getString("Contact");
                    String city = rs.getString("city");
                    m1.addRow(new Object[]{id,name,contact,city});
                }
            }
            catch(Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
        else {
            ResultSet rs = Police.searchByName(var);
            try {
                 if(!rs.next())
                    JOptionPane.showMessageDialog(this, "No record found");
                rs.beforeFirst();
                while(rs.next()) {
                    int id = rs.getInt("policeID");
                    String name = rs.getString("Name");
                    String contact = rs.getString("Contact");
                    String city = rs.getString("city");
                    m1.addRow(new Object[]{id,name,contact,city});
                }
            }
            catch(Exception e) {
                System.out.println(""+e.getMessage());
            }
        }
            // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

