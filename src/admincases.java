    private void formWindowGainedFocus(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowGainedFocus
        int crimeID = 0;
        DefaultTableModel m1 = (DefaultTableModel) jTable1.getModel();
        m1.setRowCount(0);
        ResultSet rs = Complaint.getAndAllotComplaint();
        try {
            while(rs.next()) {
                crimeID = rs.getInt("crimeID");
                Crime newCrime = Crime.getCrimeDetails(crimeID);
                int compID = rs.getInt("complaintID");
                m1.addRow(new Object[]{rs.getInt("complaintID"),newCrime.crimeName,newCrime.location,0});
            }
        }
        catch(Exception e) {
            System.out.println(""+e.getMessage());
        }// TODO add your handling code here:
    }//GEN-LAST:event_formWindowGainedFocus

