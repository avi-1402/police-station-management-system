    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String aadhar = jTextField1.getText();
        int complaintID = Integer.parseInt(jTextField2.getText());
        int pID = Person.getPersonIDthroughAadhar(aadhar);
        int cID = Complaint.getCrimeID(complaintID);
        if (Person.checkCriminalRecord(aadhar)) {
            JOptionPane.showMessageDialog(this, "Already a criminal");
            System.out.print("a "+Person.checkPrisonerRecord(aadhar));
            if(jCheckBox1.isSelected() && Person.checkPrisonerRecord(aadhar) == 0) {
                Person.enterInPrisonerRecord(pID, cID);
                JOptionPane.showMessageDialog(this, "Prisoner marked");
            }
        }
        else {
            Person.enterInCriminalRecord(pID, cID);
            JOptionPane.showMessageDialog(this, "Criminal entered");
            if(jCheckBox1.isSelected()) {
                Person.enterInPrisonerRecord(pID, cID);
                JOptionPane.showMessageDialog(this, "Prisoner marked");
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

